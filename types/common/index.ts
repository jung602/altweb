/**
 * 3D 좌표 위치를 나타내는 타입
 */
export type Position3D = [number, number, number];

/**
 * 3D 회전을 나타내는 타입 (라디안 단위)
 */
export type Rotation3D = [number, number, number];

/**
 * 3D 스케일을 나타내는 타입
 */
export type Scale3D = [number, number, number];

/**
 * 2D 크기를 나타내는 타입
 */
export type Size2D = [number, number];

